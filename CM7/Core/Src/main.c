void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	tx_buffer[0] = rx_buffer[0];
	HAL_UART_Transmit(huart, tx_buffer, 2, 100);
	int rx = atoi(rx_buffer);
	if (rx == 0) {
		HAL_I2C_Mem_Read(&hi2c1, 0b10110100, 0x00, 1, i2c_read_buffer, 1, 100);
		// tx_buffer[0] = i2c_read_buffer[0];
		uint8_t bits[9];
		itoa(i2c_read_buffer[0], bits, 2);
		bits[strlen(bits)] = '\n';
		tx_status = HAL_UART_Transmit(huart, bits, 9, 100);
	}
	else {
		i2c_write_buffer[0] = rx;
		HAL_I2C_Mem_Write(&hi2c1, 0b10110100, 0x04, 1, i2c_write_buffer, 1, 100);
		i2c_write_buffer[0] = 0x01;
		HAL_I2C_Mem_Write(&hi2c1, 0b10110100, 0x0C, 1, i2c_write_buffer, 1, 100);
	}
	HAL_UART_Receive_IT(huart, rx_buffer, 3);
}

